public class TwoD {
    public static void main(String[] args) {
        int a[][]={{5,4,3},{10,20,30},{15,17,19}};
        for(int i=0;i<=2;i++){
            for(int j=0;j<=2;j++){
                System.out.print(a[i][j]+" ");
            }
            System.out.println();
        }
    }
    
}
